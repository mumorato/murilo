    private void telasPraFrente() {
        if (cadastroPessoas != null) {
            if (cadastroPessoas.estaFechada()) {
                cadastroPessoas = null;
            } else {
                cadastroPessoas.setVisible(true);
                Toolkit.getDefaultToolkit().beep();
            }
        }
        if (cadastroCategorias != null) {
            if (cadastroCategorias.estaFechada()) {
                cadastroCategorias = null;
            } else {
                cadastroCategorias.setVisible(true);
                Toolkit.getDefaultToolkit().beep();
            }
        }        
        if (cadastroTitulos != null) {
            if (cadastroTitulos.estaFechada()) {
                cadastroTitulos = null;
            } else {
                cadastroTitulos.setVisible(true);
                Toolkit.getDefaultToolkit().beep();
            }           
        }
    }

