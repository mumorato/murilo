
public class Calculadora {
   public int somar(int a, int b){
    return a+b;
 }
   public float somar(float a, float b){
    return a+b;
 }
   
   
   public int subtrair(int a, int b){
       return a-b;
   }
   public float subtrair(float a, float b){
       return a-b;
   }   
   
   
   
   
   
}
